// Pre-build a normalized key map at module load for O(1) fallback lookups
const _normalizedMap = new Map<string, { lat: number; lon: number }>();
for (const [city, coords] of Object.entries(CITY_COORDINATES)) {
  _normalizedMap.set(normalizeName(city), coords);
}

/**
 * Get coordinates for a city name.
 * Tries exact match first, then falls back to normalized comparison
 * (handles dash/space variants like "תל אביב - יפו" vs "תל אביב-יפו").
 */
export function getCityCoords(
  cityName: string,
): { lat: number; lon: number } | null {
  return (
    CITY_COORDINATES[cityName] ??
    _normalizedMap.get(normalizeName(cityName)) ??
    null
  );
}
